    type: Object,
    required: true,
  }
});

const emit = defineEmits(['updated']);

const toast = useToastStore();
const quotation = ref(null);
const loading = ref(true);
const creatingDraft = ref(false);

const partsList = ref([]);
const calculatingRate = ref(false);

// Form definitions
const newItem = ref({
  item_type: 'product',
  part_id: '',
  service_name: '',
  price: 0,
  quantity: 1,
});

const showRemovalModal = ref(false);
const itemToRemove = ref(null);
const removalReason = ref('');
const deletingItem = ref(false);

const showApprovalModal = ref(false);
const approving = ref(false);
const approvalForm = ref({
  status: 'approved',
  approved_by: '',
  notes: '',
  approved_items: [],
  rejected_items: [],
});

const productItems = computed(() => {
  if (!quotation.value || !quotation.value.items) return [];
  return quotation.value.items.filter(i => i.item_type === 'product');
});

const serviceItems = computed(() => {
  if (!quotation.value || !quotation.value.items) return [];
  return quotation.value.items.filter(i => i.item_type === 'service');
});

const fetchQuotation = async () => {
  try {
    const response = await api.get('/quotations', { params: { job_card_id: props.jobCard.id } });
    if (response.data.data && response.data.data.length > 0) {
      // Load details of the first quotation
      const details = await api.get(`/quotations/${response.data.data[0].id}`);
      quotation.value = details.data.data;
    } else {
      quotation.value = null;
    }
  } catch (err) {
    console.error('Failed to load quotation', err);
  } finally {
    loading.value = false;
  }
};

const fetchPartsList = async () => {
  try {
